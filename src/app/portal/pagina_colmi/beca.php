        <p>Los aspirantes a postulantes a Beca Deportiva deberán presentarse con la documentación correspondiente, que acredite sus condiciones deportivas (Certificados, premios, registro de records y otros) en la fecha prescrita para cada departamento y/o región, haciendo conocer a la Comisión de Selección de Postulantes, la voluntad de acogerse a la BECA DEPORTIVA, para que el mismo sea registrado y se someta a los exámenes en el mismo lugar.</p>
