        <h4>Evaluación psicológico – vocacional.</h4>
        <p>
            El o la aspirante a Postulante debe aprobar el examen psicológico- vocacional,el mismo que le habilita para continuar con el proceso de selección.
        </p>

        <h4>Exámenes de conocimiento intelectual</h4>
        <p>
            Esta evaluación consta de 2 partes; la primera parte referida a conocimientos teóricos de las materias de Ciencias Sociales (Historia, Cívica, Geografía y Lenguaje); la segunda parte, referida exclusivamente al conocimiento de las Ciencias Exactas (Matemáticas, Física).Ambas partes deben ser APROBADAS para continuar el proceso de selección
        </p>
        <h4>Revisión médica para someterse al examen físico.</h4>
        <p>
            Para la revisión médica, todos los aspirantes a postulantes, deberán traer consigo los exámenes médicos que se describe en el cuadro que se muestra en el Cuadro No.1.Esta evaluación durante el proceso de selección y admisión de postulantes, se regirá exclusivamente a la valoración general, que habilite al aspirante a postulante para someterse al examen físico y de natación.Aquel aspirante a postulante que durante la revisión médica, no apruebe la misma, inmediatamente será separado del proceso de selección y admisión.
            <p>Los exámenes complementarios para determinar si es APTO o NO APTO para la carrera militar, deberán ser entregados al presentarse al Curso Vestibular solamente aquellos postulantes que sean aceptados a este curso.</p>
        </p>
        <h4>Examen físico. </h4>
        <p>
            La evaluación física se desarrollará de acuerdo a las marcas y tiempos establecidos en la Guía de Evaluación y Estudios (Adjunto al Prospecto de  Admisión), siendo un requisito vencer cada una de las pruebas físicas.
        </p>
        <h4>Prueba de natación. </h4>
        <p>
            Es un requisito imprescindible nadar 50 metros en la piscina (estilo Libre o Crol), no siendo permitido ningún estilo de flotación. El aspirante a postulante que NO APRUEBE será separado del proceso de selección.
        </p>

